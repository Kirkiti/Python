# operators
a=4
b=6
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)

# comparison operators
c=56
d=21

print(f"{c} is equal to {b} {c==b}")
print(f"{c} is not equal to {b} {c!=b}")
print(f"{c} is greater than {d} {c > d}")
print(f"{c} is greater than or equals to {d} {c >= d}")
print(f"{c} is less than {d} {c < d}")
print(f"{c} is less than or equal to {d} {c <= d}")

#logical operator
e=7
print(e>4 and e>7)
print(e>4 or e<7)

